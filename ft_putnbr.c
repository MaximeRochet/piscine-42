#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int	res;
	char		aff;

	res = nb;
	if (res < 0)
	{
		res *= -1;
		write(1, "-", 1);
	}
	if (res >= 10)
		ft_putnbr(res / 10);
	aff = res % 10 + '0';
	write(1, &aff, 1);
}
